class PrimitiveDatatypes {
    public static void main(String[] args) {
        int intMaxRange = Integer.MAX_VALUE;
        int intMinRange = Integer.MIN_VALUE;
        System.out.println(intMaxRange);
        System.out.println(intMinRange);

        byte byteMaxRange = Byte.MAX_VALUE;
        byte byteMinRange = Byte.MIN_VALUE;
        System.out.println(byteMaxRange);
        System.out.println(byteMinRange);

        short shortMaxRange = Short.MAX_VALUE;
        short shortMinRange = Short.MIN_VALUE;
        System.out.println(shortMaxRange);
        System.out.println(shortMinRange);

        long longMaxRange = Long.MAX_VALUE;
        long longMinRange = Long.MIN_VALUE;
        System.out.println(longMaxRange);
        System.out.println(longMinRange);

        float floatMaxRange = Float.MAX_VALUE;  
        float floatMinRange = Float.MIN_VALUE;
        System.out.println(floatMaxRange);
        System.out.println(floatMinRange);

        double doubleMaxRange = Double.MAX_VALUE;
        double doubleMinRange = Double.MIN_VALUE;
        System.out.println(doubleMaxRange);
        System.out.println(doubleMinRange);
    }
}
